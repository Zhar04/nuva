/// Compute the 4-letter type from per-question answers (-2..2, same order).
String computeMbti(List<int> answers) {
  final net = {'EI': 0, 'SN': 0, 'TF': 0, 'JP': 0};
  for (var i = 0; i < mbtiQuestions.length && i < answers.length; i++) {
    final q = mbtiQuestions[i];
    net[q.axis] = net[q.axis]! + q.sign * answers[i];
  }
  return (net['EI']! >= 0 ? 'E' : 'I') +
      (net['SN']! >= 0 ? 'S' : 'N') +
      (net['TF']! >= 0 ? 'T' : 'F') +
      (net['JP']! >= 0 ? 'J' : 'P');
}

