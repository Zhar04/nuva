  final String axis; // 'EI' | 'SN' | 'TF' | 'JP'
  final int sign; // +1: "agree" → first letter (E/S/T/J); -1: → second (I/N/F/P)
  const MbtiQuestion(this.text, this.axis, this.sign);
}

